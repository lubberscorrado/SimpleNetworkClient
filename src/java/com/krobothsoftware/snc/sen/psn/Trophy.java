package com.krobothsoftware.snc.sen.psn;

/**
 * Trophy Types used with PSN.
 *
 * @author Kyle Kroboth
 * @since SEN-PSN 1.0
 */
public enum Trophy {

    /**
     * Platinum trophy type.
     *
     * @since SEN-PSN 1.0
     */
    PLATINUM,

    /**
     * Gold trophy type.
     *
     * @since SEN-PSN 1.0
     */
    GOLD,

    /**
     * Silver trophy type.
     *
     * @since SEN-PSN 1.0
     */
    SILVER,

    /**
     * Bronze trophy type.
     *
     * @since SEN-PSN 1.0
     */
    BRONZE,

    /**
     * Hidden trophy type.
     *
     * @since SEN-PSN 1.0
     */
    HIDDEN;

}
